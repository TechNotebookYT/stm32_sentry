/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  // initial message buffer empty (for debugging console output)
  char msgBuffer[52];
  for (uint8_t i = 0; i < 52; i++)
  {
    msgBuffer[i] = ' ';
  }

  // LCD CODE
  lcd_init();

  // initialize distance laser
  statInfo_t_VL53L0X distanceStr;
  initVL53L0X(1, &hi2c1);

  // 20cm
  setSignalRateLimit(200);
  setVcselPulsePeriod(VcselPeriodPreRange, 10);
  setVcselPulsePeriod(VcselPeriodFinalRange, 14);
  setMeasurementTimingBudget(300 * 1000UL);
  uint16_t distance;

  // initial display content
  lcd_put_cursor(0, 0);
  lcd_send_string("Welcome to");
  lcd_put_cursor(1, 0);
  lcd_send_string("STM32 Sentry");
  HAL_Delay(1000);

  lcd_clear();
  lcd_put_cursor(0, 0);
  lcd_send_string("All Clear");

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  int obj_sector[4] = {0, 0, 0, 0}; // split 180 degree region into 4 sectors
  int sweepAngle = 0;
  Set_Servo_Angle(&htim2, TIM_CHANNEL_1, sweepAngle);

  uint32_t lastSweepTick = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    /* USER CODE BEGIN 3 */

    switch (current_state)
    {
    case IDLE:
      if (motionDetected)
      {
        for (int i = 0; i < 4; i++)
        {
          obj_sector[i] = 0;
        }
        motionDetected = 0;
        current_state = SCAN;
      }
      break;

    case SCAN:
      // 200ms Non-blocking Timer
      if (HAL_GetTick() - lastSweepTick >= 200)
      {
        lastSweepTick = HAL_GetTick();

        // READ sensor & output
        distance = readRangeSingleMillimeters(&distanceStr);
        sprintf(msgBuffer, "Ang: %d | Dist: %d\r\n", sweepAngle, distance);
        HAL_UART_Transmit(&huart2, (uint8_t *)msgBuffer, strlen(msgBuffer), 100);

        // Data processing
        if (distance > 0 && distance < 160)
        {
          int sector_index = sweepAngle / 45;
          if (sector_index > 3)
            sector_index = 3;
          obj_sector[sector_index] = 1;
        }

        // 3. Move Servo motor
        sweepAngle += 10;

        if (sweepAngle > 180)
        {
          current_state = NOTIFY;
          sweepAngle = 0;
          Set_Servo_Angle(&htim2, TIM_CHANNEL_1, sweepAngle);
          HAL_Delay(400);
        }
        else
        {
          Set_Servo_Angle(&htim2, TIM_CHANNEL_1, sweepAngle);
        }
      }
      break;

    case NOTIFY:
    {
      bool foreign_objects = false;

      // Check if any sectors are tripped
      for (int i = 0; i < 4; i++)
      {
        if (obj_sector[i])
        {
          foreign_objects = true;
        }
      }

      // Update Display
      lcd_clear(); // Clear the prev. message
      lcd_put_cursor(0, 0);

      if (foreign_objects)
      {
        lcd_send_string("OBJECTS FOUND:");

        lcd_put_cursor(1, 0);
        for (int i = 0; i < 4; i++)
        {
          if (obj_sector[i])
          {
            lcd_send_string("[X] "); // X marks an object in that sector
          }
          else
          {
            lcd_send_string("[ ] "); // Empty sector
          }
        }

        current_state = SCAN;
        for (int i = 0; i < 4; i++)
        {
          obj_sector[i] = 0;
        }
      }
      else
      {
        current_state = IDLE;
        lcd_put_cursor(0, 0);
        lcd_send_string("STM32 Sentry");
        lcd_put_cursor(1, 0);
        lcd_send_string("All Clear");

        uint32_t startWait = HAL_GetTick();
        while (HAL_GetTick() - startWait < 1000)
        {
          if (motionDetected)
          {
            break;
          }
        }
      }
      break;
    }
    }
  }
  /* USER CODE END 3 */
}

